#Program for working with Tuple
tuple = ( 'abcd', 786 , 2.23, 'jekek', 70.2 )
tinytuple = (123, 'Disha')
print (tuple) # Prints complete list
print (tuple[0]) # Prints first element of the list
print (tuple[1:3]) # Prints elements starting from 1st till 2nd
print (tuple[2:]) # Prints elements starting from 2nd element
print (tuple[:2]) # Prints elements upto 1st element
print (tinytuple * 2) # Prints list two times
print (tuple + tinytuple)# Prints concatenated lists
